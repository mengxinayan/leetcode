# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def insertionSortList(self, head: ListNode) -> ListNode:
        arr = []
        curr = head
        while curr != None:
            arr.append(curr.val)
            curr = curr.next
        arr.sort()
        dummy = ListNode(0)
        curr = dummy
        for num in arr:
            tmp = ListNode(num)
            curr.next = tmp
            curr = curr.next
        return dummy.next

