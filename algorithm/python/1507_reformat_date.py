class Solution:
    def reformatDate(self, date: str) -> str:
        dates = date.split(' ')
        day = month = year = ''
        if len(dates[0]) == 3:
            day = '0' + dates[0][0]
        else:
            day = dates[0][:2]
        month_dict = {'Jan': '01', 'Feb': '02', 'Mar': '03', 'Apr': '04', 'May': '05', 'Jun': '06', 'Jul': '07', 'Aug': '08', 'Sep': '09', 'Oct': '10', 'Nov': '11', 'Dec': '12'}
        month = month_dict[dates[1]]
        year = dates[2]
        res = year + '-' + month + '-' + day
        return res

