class OrderedStream:

    def __init__(self, n: int):
        self.ptr = 1
        self.id_value_dict = {}

    def insert(self, id: int, value: str) -> List[str]:
        self.id_value_dict[id] = value
        ans = []
        while self.ptr in self.id_value_dict:
            ans.append(self.id_value_dict[self.ptr])
            self.ptr += 1
        return ans


