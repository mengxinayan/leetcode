# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def minDiffInBST(self, root: TreeNode) -> int:
        
        def in_order(root: TreeNode):
            if root.left != None:
                in_order(root.left)
            arr.append(root.val)
            if root.right != None:
                in_order(root.right)

        arr = []
        in_order(root)
        res = float('inf')
        for i in range(0, len(arr)-1):
            res = min(res, arr[i+1]-arr[i])
        return res

