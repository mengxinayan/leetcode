# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def detectCycle(self, head: ListNode) -> ListNode:
        node_set = set()
        tmp = head
        while tmp != None:
            if tmp not in node_set:
                node_set.add(tmp)
            else:
                break
            tmp = tmp.next
        return tmp

