class Solution:
    def decompressRLElist(self, nums: List[int]) -> List[int]:
        ans = []
        i = 0
        while i != len(nums):
            length = nums[i]
            num = nums[i+1]
            ans += [num for i in range(length)]
            i += 2
        return ans

