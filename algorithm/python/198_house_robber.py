class Solution:
    def rob(self, nums: List[int]) -> int:
        if len(nums) == 0:
            return 0
        elif len(nums) == 1:
            return nums[0]
        elif len(nums) == 2:
            return max(nums[0], nums[1])
        else:
            ans = [nums[0], max(nums[0], nums[1])]
            for i in range(2,len(nums)):
                ans.append(max(ans[i-2]+nums[i], ans[i-1]))
            return ans[-1]

